    def resDosNum(self,num1,num2):
        return num1-num2

    def multDosNum(self,num1,num2):
        return num1*num2

    def divDosNum(self,num1,num2):
        return num1/num2

    def NumPI(numPI):
        return math.pi

    def NumPHI(numPHI):
        return 1.16e-4

